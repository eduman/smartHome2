package it.eduman.mobileHome2.weather;

public class Wind {

	private float speed;
	private float deg;
	private float var_beg;
	private float var_end;
	public float getSpeed() {
		return speed;
	}
	public void setSpeed(float speed) {
		this.speed = speed;
	}
	public float getDeg() {
		return deg;
	}
	public void setDeg(int deg) {
		this.deg = deg;
	}
	public float getVar_beg() {
		return var_beg;
	}
	public void setVar_beg(int var_beg) {
		this.var_beg = var_beg;
	}
	public float getVar_end() {
		return var_end;
	}
	public void setVar_end(int var_end) {
		this.var_end = var_end;
	}
	
	
}
